    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        ArrayList<Cliente> listaClientes;
        try {
            listaClientes = ClienteDAO.pesquisaSimples(txtnomeCliente.getText());

            DefaultTableModel tabela = (DefaultTableModel) tblClientes.getModel();
            
            tabela.setRowCount(0);
            for (Cliente c : listaClientes) {
                tabela.addRow(new Object[]{c.getCodcli(), c.getNome(), c.getCpf()});
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        int linhaSelecionada = tblClientes.getSelectedRow();
            Cliente cliente = new Cliente();
            cliente.setCodcli((int) tblClientes.getModel().getValueAt(linhaSelecionada, 0));
            cliente.setNome(tblClientes.getModel().getValueAt(linhaSelecionada, 1).toString());
            cliente.setCpf(tblClientes.getModel().getValueAt(linhaSelecionada, 2).toString());

            if (parent instanceof TelaVenda) {
                TelaVenda lv = (TelaVenda) parent;
                lv.setCliente(cliente);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Campo de destino inv√°lido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            }
    }//GEN-LAST:event_tblClientesMouseClicked



